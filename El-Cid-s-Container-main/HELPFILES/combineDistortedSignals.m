function combined_signal = combineDistortedSignals(distorted_signal1, distorted_signal2)

%This MATLAB function combineDistortedSignals combines two distorted signals into a single combined signal.
%distorted_signal1: This parameter represents the first distorted signal.
%distorted_signal2: This parameter represents the second distorted signal.
%The function performs the following task:
%Combine Signals: It adds the two distorted signals together to create a combined signal.














% Combine distorted signals
    combined_signal = distorted_signal1 + distorted_signal2;
end